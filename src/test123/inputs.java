      public void setinput(){
        ResultSet r = k.select(this.id);
        
        try {
            while(r.next()){
            input_nama.setText(r.getString("nama"));
            input_nim.setText(r.getString("nim"));
            input_jurusan.setText(r.getString("jurusan"));
            input_angkatan.setText(r.getString("angkatan").substring(0, 4));
            }
        } catch (SQLException ex) {
            Logger.getLogger(inputs.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
