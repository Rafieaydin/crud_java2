    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        mahassiwa ms = new mahassiwa();
        ms.setNama(input_nama.getText());
        ms.setNim(input_nim.getText());
        ms.setJurusan(input_jurusan.getText());
        //System.out.println(input_angkatan.getText().substring(0,4)); // 2023
        ms.setAngkatan(Integer.parseInt(input_angkatan.getText()));
        int ouput = -1;
        if(this.id > -1){
            ouput = k.update(ms, this.id);
        
        }else{
            ouput = k.insert(ms);
        }
        
        if (ouput != -1){
            JOptionPane.showMessageDialog(this,"Data berhasil di submit");
            crud c = new crud();
            c.setVisible(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(this,"Data gagal di submit", "Validasi", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_btn_submitActionPerformed

